import turtle

turtle.pensize(10)

turtle.color("blue")
turtle.penup()
turtle.goto(-300,150)
turtle.pendown()
turtle.circle(100)

turtle.color("black")
turtle.penup()
turtle.goto(-50,150)
turtle.pendown()
turtle.circle(100)

turtle.color("red")
turtle.penup()
turtle.goto(200,150)
turtle.pendown()
turtle.circle(100)

turtle.color("yellow")
turtle.penup()
turtle.goto(-170,250)
turtle.left(180)
turtle.pendown()
turtle.circle(100)

turtle.color("green")
turtle.penup()
turtle.goto(50,250)
turtle.pendown()
turtle.circle(100)

turtle.done()