public class Vehicle {
    private int tires;
    private String color;
    private String model;

    //tire getter
    public int getTires(){
        return tires;
    }

    //tire setter
    public void setTires(int t){
        this.tires = t;
    }

    //color getter 
    public String getColor(){
        return color;
    }
    
    //color setter
    public void setColor(String c){
        this.color = c;
    }

    //model getter 
    public String getModel(){
        return model;
    }

    //model  setter
    public void setModel(String m){
        this.model = m;
    }
    
}
